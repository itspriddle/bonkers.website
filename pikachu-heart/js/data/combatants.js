// Create a runtime combatant from a template
export function createCombatant(template, isEnemy = false) {
  return {
    id: template.id,
    name: template.name,
    hp: template.hp,
    maxHp: template.maxHp,
    speed: template.speed,
    power: template.power,
    sprite: template.sprite,
    attackSprite: template.attackSprite,
    hurtSprite: template.hurtSprite,
    cheerSprite: template.cheerSprite || template.sprite,
    defeatSprite: template.defeatSprite || null,
    rageSprite: template.rageSprite || null,
    napSprite: template.napSprite || null,
    spriteW: template.spriteW,
    spriteH: template.spriteH,
    isEnemy,
    status: null,
    statusTurns: 0,
    alive: true,
    abilities: template.abilities || null,
    hidden: template.hidden || null,
    moves: template.moves || null,
    immune: template.immune || false,
    currentSprite: template.sprite,
    spriteSizes: template.spriteSizes || null,
  };
}
